    private void btnCalcGpaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCalcGpaMouseClicked
        //Declare Variables
        double dTotalCredits;
        double dGpa;
        DecimalFormat dFormat = new DecimalFormat("0.00");
        
        
        //This adds up all the credits from each text box.
        dTotalCredits = Double.parseDouble(txt110.getText()) + Double.parseDouble(txt201.getText()) + Double.parseDouble(txt303.getText()) + 
                Double.parseDouble(txtAcc200.getText()) + Double.parseDouble(txtFin201.getText()) + Double.parseDouble(txtBusm241.getText());
        
        //This is the final gpa by passing all the information into the calcGpa method and then dividing it by the number of credits.
        dGpa = (calcGpa(cmb110.getSelectedItem().toString(), Double.parseDouble(txt110.getText())) + calcGpa(cmb201.getSelectedItem().toString(), Double.parseDouble(txt201.getText())) +
                calcGpa(cmb303.getSelectedItem().toString(), Double.parseDouble(txt303.getText())) + calcGpa(cmbAcc200.getSelectedItem().toString(), Double.parseDouble(txtAcc200.getText()))
                + calcGpa(cmbFin201.getSelectedItem().toString(), Double.parseDouble(txtFin201.getText())) + calcGpa(cmbBusm241.getSelectedItem().toString(), Double.parseDouble(txtBusm241.getText()))) / dTotalCredits;
        
        lblGpa.setText(String.valueOf(dFormat.format(dGpa)));
        
        //this if statement decides which color shows.
        if(dGpa >= 3.7)
        {
            panGreen.setVisible(true);
        }
        else if(dGpa >= 3.2)
        {
            panYellow.setVisible(true);
        }
        else
        {
            panRed.setVisible(true);
        }
        
    }//GEN-LAST:event_btnCalcGpaMouseClicked

