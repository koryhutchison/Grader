    //The calcGpa method. It takes the letter grade, and then 
    private double calcGpa(String sLetterGrade, double dCredit)
    {
        double dGrade = 0;
        //This determines the grade score for the grade in the combo box
        if(sLetterGrade.equalsIgnoreCase("A"))
            {
                dGrade = 4;
            }
            else if(sLetterGrade.equalsIgnoreCase("A-"))
            {
                dGrade = 3.7;
            }
            else if(sLetterGrade.equalsIgnoreCase("B+"))
            {
                dGrade = 3.4;
            }
            else if(sLetterGrade.equalsIgnoreCase("B"))
            {
                dGrade = 3;
            }
            else if(sLetterGrade.equalsIgnoreCase("B-"))
            {
                dGrade = 2.7;
            }
            else if(sLetterGrade.equalsIgnoreCase("C+"))
            {
                dGrade = 2.4;
            }
            else if(sLetterGrade.equalsIgnoreCase("C"))
            {
                dGrade = 2;
            }
            else if(sLetterGrade.equalsIgnoreCase("C-"))
            {
                dGrade = 1.7;
            }
            else if(sLetterGrade.equalsIgnoreCase("D+"))
            {
                dGrade = 1.4;
            }
            else if(sLetterGrade.equalsIgnoreCase("D"))
            {
                dGrade = 1;
            }
            else if(sLetterGrade.equalsIgnoreCase("D-"))
            {
                dGrade = .7;
            }
            else if(sLetterGrade.equalsIgnoreCase("E"))
            {
                dGrade = 0;
            }
        //This then returns the credits multiplied by the leter grade. This will then be divided by the total credits.
        return dCredit * dGrade;
    }
    
    
